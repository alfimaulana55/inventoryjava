    private void j_produkItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_j_produkItemStateChanged
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)dataOrder.getModel();
        barisCell = dataOrder.getSelectedRow();
        try{
            //cara agar diset 0 setelah klik combobox
            dataOrder.setValueAt(null, barisCell, 3);
            dataOrder.setValueAt(null, barisCell, 4);
            
            conn = koneksi.koneksi();
            nama_barang_harga = j_produk.getSelectedItem().toString();  
            String bacaSemuaData = "SELECT kd_barang, harga FROM barang where nama_barang='"+nama_barang_harga+"'";
            pst = conn.prepareStatement(bacaSemuaData);
            res = pst.executeQuery();
            while(res.next()){
                id_products = res.getString(1);
                harga_ambil = res.getString(2);
                model.setValueAt(id_products, barisCell, 0);
                model.setValueAt(harga_ambil, barisCell, 2);
                //t_harga.setText(value);
            }
        }catch(Exception e){
        }
    }//GEN-LAST:event_j_produkItemStateChanged

