    public void Produk(){
        try{
            j_produk.removeAllItems();
            j_produk.addItem("");
            conn = koneksi.koneksi();
            String bacaSemuaData = "SELECT kd_barang, nama_barang, harga, stok_barang FROM barang";
            pst = conn.prepareStatement(bacaSemuaData);
            res = pst.executeQuery();
            while(res.next()){
                all_products = res.getString(2);
                stok_barang = res.getString(4);
                j_produk.addItem(all_products);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }
    
    public void autonumberDetailOrder(){
        try{
            String an = "SELECT MAX(no_order) FROM detail_order";
            pst = conn.prepareStatement(an);
            res = pst.executeQuery();
            if (res.next()){
                int a = res.getInt(1);
                auto_num=(Integer.toString(a+1));
            }
            res.close();
            pst.close();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
